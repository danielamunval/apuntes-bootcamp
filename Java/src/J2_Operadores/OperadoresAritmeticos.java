package J2_Operadores;

public class OperadoresAritmeticos {

    /*** OPERADORES ARITMÉTICOS ***/
    /*
    + -> suma
    - -> resta
    * -> multiplicación
    / -> división
    % -> Módulo o resto.

     */
    int numeroUno = 10;
    int numeroDos = 5;

    //Variables de resultado
    int resultadoSuma = numeroUno + numeroDos; //15
    int resultadoResta = numeroUno - numeroDos; //5
    int resultadoMultiplicacion = numeroUno * numeroDos; //50
    int resultadoDivision = numeroUno / numeroDos; //2
    int restoDivision = numeroUno % numeroDos; //0

}
