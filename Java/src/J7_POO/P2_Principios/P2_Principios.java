package J7_POO.P2_Principios;

public class P2_Principios {

    // Los principios de la programación orientada a objetos (POO) se aplican en varios lenguajes de programación orientados a objetos. Son conceptos generales.

    /* ENCAPSULAMIENTO
    -> Es la ocultación de los detalles internos de un objeto y la restricción del acceso directo a sus atributos.
    -> El encapsulamiento facilita el diseño de clases que son más seguras, flexibles y mantenibles.
    -> En Java, se logra a través del uso de modificadores de acceso (private, protected, public) y métodos de acceso (getters y setters).

    */

    /* HERENCIA
    -> Permite la creación de una nueva clase basada en una clase existente, aprovechando sus propiedades y comportamientos.
    -> La clase existente se llama "clase base" o "clase padre", y la nueva clase se llama "clase derivada" o "clase hija". Es así que literalmente "hereda" sus propiedades y comportamientos.

     */

    /* POLIMORFISMO
    -> Es la capacidad de un objeto de tomar muchas formas.
    -> El polimorfismo se manifiesta principalmente a través de dos maneras: la sobrecarga y la sobreescritura de métodos.

    */

    /* ABSTRACCIÓN
    -> Es el proceso de simplificar y modelar la realidad, centrándose en las características más importantes y omitiendo los detalles innecesarios.
    -> Representa los aspectos esenciales de un objeto del mundo real sin preocuparse por los detalles específicos que no son relevantes para el propósito del programa.
    -> En Java, logramos la abstracción a través de clases abstractas, métodos abstractos e interfaces.

    */

}
