package J4_Bucles;

public class B1_For {
    /** BUCLE FOR **/
    // Permite repetir un bloque de código un número específico de veces.
    // Es especialmente útil cuando se conoce de antemano la cantidad exacta de iteraciones que se deben realizar. Por ejemplo, al recorrer un array.

    /** Sintaxis básica:
    for (inicialización; condición; actualización) {
        // Bloque de código a repetir
    }

     - Inicialización: Se realiza una vez al comienzo del bucle y generalmente se utiliza para inicializar una variable de control.
     - Condición: Se evalúa antes de cada iteración. Si la condición es verdadera, se ejecuta el bloque de código. Si la condición es falsa, el bucle se sale.
     - Actualización: Se realiza después de cada iteración y generalmente se utiliza para actualizar la variable de control.
     - Bloque de código: Es el conjunto de declaraciones que se ejecutan en cada iteración del bucle.

     **/


}
