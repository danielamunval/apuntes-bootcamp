package J4_Bucles;

public class B3_DoWhile {
    /** DO WHILE **/

    // Permite ejecutar un bloque de código al menos una vez, y luego repetir la ejecución mientras una condición específica sea verdadera.

    /** Sintaxis básica

     do {
     // Bloque de código a repetir al menos una vez
     } while (condicion);

     - Bloque de código: Es el conjunto de declaraciones que se ejecuta al menos una vez, independientemente de la condición.
     - Condicion: Es una expresión booleana que se evalúa después de cada iteración. Si la condición es verdadera, el bucle se repetirá.
     **/

    /** ¿Cuál es su diferencia con el bucle While? **/

    //A diferencia del bucle while tradicional, el bucle do-while garantiza que el bloque de código se ejecute al menos una vez, incluso si la condición es inicialmente falsa.


}
